namespace HVO.Astronomy
{
    /// <summary>
    /// Specifies the latitude hemisphere (north or south of the equator).
    /// </summary>
    public enum LatitudeHemisphere
    {
        /// <summary>North of the equator.</summary>
        North,

        /// <summary>South of the equator.</summary>
        South
    }

    /// <summary>
    /// Specifies the longitude hemisphere (east or west of the prime meridian).
    /// </summary>
    public enum LongitudeHemisphere
    {
        /// <summary>East of the prime meridian.</summary>
        East,

        /// <summary>West of the prime meridian.</summary>
        West
    }
}
